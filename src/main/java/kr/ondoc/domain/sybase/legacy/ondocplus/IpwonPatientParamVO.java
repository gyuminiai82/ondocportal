package kr.ondoc.domain.sybase.legacy.ondocplus;

public class IpwonPatientParamVO {
	String regno = "";
	String kind = "";
	String medroom = "";
	String reg_year = "";
	String ch_year = "";
	String date = "";
	
	public String getRegno() {
		return regno;
	}
	public void setRegno(String regno) {
		this.regno = regno;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	public String getMedroom() {
		return medroom;
	}
	public void setMedroom(String medroom) {
		this.medroom = medroom;
	}
	public String getReg_year() {
		return reg_year;
	}
	public void setReg_year(String reg_year) {
		this.reg_year = reg_year;
	}
	public String getCh_year() {
		return ch_year;
	}
	public void setCh_year(String ch_year) {
		this.ch_year = ch_year;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
