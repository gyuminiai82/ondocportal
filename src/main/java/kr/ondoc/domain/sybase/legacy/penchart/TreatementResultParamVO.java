package kr.ondoc.domain.sybase.legacy.penchart;

public class TreatementResultParamVO {
	String ptno = "";
	String year = "";
	String date = "";
	String ord_seq = "";
	String code = "";
	String edi_code = "";
	
	public String getPtno() {
		return ptno;
	}
	public void setPtno(String ptno) {
		this.ptno = ptno;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getOrd_seq() {
		return ord_seq;
	}
	public void setOrd_seq(String ord_seq) {
		this.ord_seq = ord_seq;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getEdi_code() {
		return edi_code;
	}
	public void setEdi_code(String edi_code) {
		this.edi_code = edi_code;
	}
}
