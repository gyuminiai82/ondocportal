package kr.ondoc.domain.sybase.crm;

public class CrmOpcodeVO {
	String cop_code = "";
	String cop_name = "";
	String cop_seq = "";
	String cop_use = "";
	
	public String getCop_code() {
		return cop_code;
	}
	public void setCop_code(String cop_code) {
		this.cop_code = cop_code;
	}
	public String getCop_name() {
		return cop_name;
	}
	public void setCop_name(String cop_name) {
		this.cop_name = cop_name;
	}
	public String getCop_seq() {
		return cop_seq;
	}
	public void setCop_seq(String cop_seq) {
		this.cop_seq = cop_seq;
	}
	public String getCop_use() {
		return cop_use;
	}
	public void setCop_use(String cop_use) {
		this.cop_use = cop_use;
	}
}
