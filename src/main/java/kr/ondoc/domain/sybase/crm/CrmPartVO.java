package kr.ondoc.domain.sybase.crm;

public class CrmPartVO {
	String cpu_code = "";
	String cpu_name = "";
	String cpu_seq = "";
	String cpu_use = "";
	
	public String getCpu_code() {
		return cpu_code;
	}
	public void setCpu_code(String cpu_code) {
		this.cpu_code = cpu_code;
	}
	public String getCpu_name() {
		return cpu_name;
	}
	public void setCpu_name(String cpu_name) {
		this.cpu_name = cpu_name;
	}
	public String getCpu_seq() {
		return cpu_seq;
	}
	public void setCpu_seq(String cpu_seq) {
		this.cpu_seq = cpu_seq;
	}
	public String getCpu_use() {
		return cpu_use;
	}
	public void setCpu_use(String cpu_use) {
		this.cpu_use = cpu_use;
	}
}
